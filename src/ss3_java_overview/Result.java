package ss3_java_overview;

public class Result {
    public static void main(String[] args) {
        // Khoi tao bien i voi gia tri 2
        int i = 2;

        // In gia tri lan dau
        System.out.println("Kết quả của i++: " + i++);
        System.out.println("Kết quả của ++i: " + ++i);

        // In gia tri lan nua
        System.out.println("Kết quả của i++ lần nữa: " + i++);
        System.out.println("Kết quả của ++i lần nữa: " + ++i);

        // In gia tri lan cuoi
        System.out.println("Giá trị cuối cùng của i: " + i);
    }
}
